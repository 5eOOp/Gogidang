public class ReplyNoticeVO {
	
	private int s_comment_num;
	private int qs_num;
	private String u_id;
	private String re_content;
	private String re_date;
	private String s_num;
	
	public int getS_comment_num() {
		return s_comment_num;
	}
	public void setS_comment_num(int s_comment_num) {
		this.s_comment_num = s_comment_num;
	}
	public int getQs_num() {
		return qs_num;
	}
	public void setQs_num(int qs_num) {
		this.qs_num = qs_num;
	}
	public String getU_id() {
		return u_id;
	}
	public void setU_id(String u_id) {
		this.u_id = u_id;
	}
	public String getRe_content() {
		return re_content;
	}
	public void setRe_content(String re_content) {
		this.re_content = re_content;
	}
	public String getRe_date() {
		return re_date;
	}
	public void setRe_date(String re_date) {
		this.re_date = re_date;
	}
	public String getS_num() {
		return s_num;
	}
	public void setS_num(String s_num) {
		this.s_num = s_num;
	}
	
}
